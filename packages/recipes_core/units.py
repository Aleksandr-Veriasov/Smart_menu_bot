"""Допустимые единицы измерения ингредиентов.

ALLOWED_UNITS — канонический список, используется в промпте LLM и валидации WebApp-редактора.
normalize_unit() — приводит пользовательский ввод и LLM-ответ к каноническому значению.
"""

ALLOWED_UNITS: list[str] = [
    "г",
    "кг",
    "мл",
    "л",
    "ст.л.",
    "ч.л.",
    "стакан",
    "шт",
    "пучок",
    "щепотка",
    "по вкусу",
]

_ALIASES: dict[str, str] = {
    "гр": "г",
    "грамм": "г",
    "граммов": "г",
    "килограмм": "кг",
    "килограммов": "кг",
    "миллилитр": "мл",
    "миллилитров": "мл",
    "литр": "л",
    "литров": "л",
    "столовая ложка": "ст.л.",
    "столовых ложки": "ст.л.",
    "столовых ложек": "ст.л.",
    "чайная ложка": "ч.л.",
    "чайных ложки": "ч.л.",
    "чайных ложек": "ч.л.",
    "штука": "шт",
    "штуки": "шт",
    "штук": "шт",
}


def normalize_unit(unit: str | None) -> str | None:
    """Приводит единицу измерения к каноническому виду из ALLOWED_UNITS.

    Неизвестные единицы возвращаются as-is (без потери данных).
    """
    if not unit:
        return None
    normalized = unit.strip().lower()
    return _ALIASES.get(normalized, unit.strip())
